#include <iostream>
using namespace std;
int main()
{
  int a, b;
  cin >> a >> b;
  cout << "Sum of " << a << " and " << b << " is " << a + b << endl;
  cout << "Difference of " << a << " and " << b << " is " << a - b << endl;
  cout << "Product of " << a << " and " << b << " is " << a * b << endl;
  cout << "Quotient of " << a << " and " << b << " is " << a / b << endl;
}